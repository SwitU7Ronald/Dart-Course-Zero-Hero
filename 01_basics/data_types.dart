// Shows Dart's basic data types

void main() {
  int year = 2025;
  double temperature = 36.6;
  String greeting = 'Hello, Dart!';
  bool isActive = true;

  print('Year: $year');
  print('Temperature: $temperature');
  print('Greeting: $greeting');
  print('Is Active? $isActive');
}

/*
Expected Output:
Year: 2025
Temperature: 36.6
Greeting: Hello, Dart!
Is Active? true
*/
