// Demonstrates how to declare variables and constants in Dart

void main() {
  // Variable (mutable)
  var name = 'Kshitij';
  int age = 25;

  // Constant (immutable)
  const country = 'India';
  final city = 'Nadiad';

  print('Name: $name');
  print('Age: $age');
  print('Country: $country');
  print('City: $city');
}

/*
Expected Output:
Name: Kshitij
Age: 25
Country: India
City: Nadiad
*/
